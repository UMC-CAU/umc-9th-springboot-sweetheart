package com.example.umc9th.global.response.code;

/**
 * 모든 응답 코드가 구현해야 하는 기본 인터페이스
 *
 * 이 인터페이스를 구현함으로써 성공/실패 응답 코드의 일관성을 보장합니다.
 * SuccessCode, ErrorCode enum들이 이 인터페이스를 구현하여 동일한 구조를 가지게 됩니다.
 */
public interface BaseCode {

    /**
     * HTTP 상태 코드를 반환합니다.
     *
     * @return HTTP 상태 코드 (예: 200, 400, 404, 500 등)
     *
     * 예시:
     * - 200: 성공
     * - 400: 잘못된 요청
     * - 404: 리소스를 찾을 수 없음
     * - 500: 서버 내부 오류
     */
    int getStatus();

    /**
     * 응답 코드를 반환합니다.
     *
     * @return 응답 코드 문자열 (예: "MEMBER_200", "COMMON_400")
     *
     * 코드 네이밍 규칙:
     * - {도메인}_{HTTP상태코드} 형식을 사용
     * - 예: MEMBER_200, MEMBER_404, COMMON_500
     *
     * 이렇게 하면 프론트엔드에서 에러를 구체적으로 식별할 수 있습니다.
     */
    String getCode();

    /**
     * 사용자에게 보여줄 메시지를 반환합니다.
     *
     * @return 응답 메시지 (예: "회원 조회 성공", "회원을 찾을 수 없습니다")
     *
     * 메시지 작성 가이드:
     * - 명확하고 이해하기 쉬운 한글 메시지 사용
     * - 프론트엔드에서 사용자에게 그대로 보여줄 수 있는 수준으로 작성
     */
    String getMessage();
}
